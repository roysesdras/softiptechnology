        <h3>PAYS D’IMPLANTATION</h3>
            <p style="text-align:justify;">
              État d'Afrique centrale ouvert au sud-ouest sur l'océan Atlantique, le Congo est limité au sud par l'Angola, à l'est par la République démocratique du Congo, au nord par la République centrafricaine, au nord-ouest par le Cameroun et à l'ouest par le Gabon. <br><br>

              <b>Superficie :</b> 342 000 km2 <br>
              <b>Nombre d'habitants :</b> 5 380 508 (estimation pour 2019)<br>
              <b>Nom des habitants :</b> Congolais <br>
              <b>Capitale :</b> Brazzaville <br>
              <b>Langue :</b> français <br>
              <b>Monnaie :</b> franc CFA <br>
              <b>Chef de l'État :</b> Denis Sassou-Nguesso <br>
              <b>Chef du gouvernement :</b> Anatole Collinet Makosso <br>
              <b>Constitution :</b> <br>
              <b>Adoption :</b> 25 octobre 2015 <br>
              <b>Entrée en vigueur :</b> 6 novembre 2015 <br> <br>

              Chevauchant l'équateur, le pays est en grande partie recouvert par la forêt dense, localement exploitée. Le manioc est la base de l'alimentation. Le pétrole est la principale source d'exportations. Les deux seules grandes ville sont la capitale, Brazzaville, et le port de Pointe-Noire. <br> <br>

              <b>LE MILIEU NATUREL</b><br>
              <b>LE RELIEF</b><br>
              D'une superficie de 342 000 km2, l'État s'étire du sud-ouest au nord-est sur 1 200 km, à partir d'une façade maritime étroite. Au nord, une partie de la cuvette alluviale du Congo, plate et souvent marécageuse, draine un large éventail de rivières ; sur le pourtour se succèdent des plateaux et des collines gréseuses qui culminent vers 860 m dans le pays Téké, tandis que le socle ancien apparaît dans le bassin de l'Ivindo. Dans le sud, au-delà des collines du Pool, s'ouvre le bassin Niari-Nyanga encadré par le plateau des Cataractes, À l'ouest de Brazzaville, le massif granito-gneissique du Chaillu est cerné par des auréoles de couches sédimentaires dessinant une ample dépression (synclinal Niari-Nyanga). Ces couches forment des plis serrés contre la chaîne du Mayombe, dont les crêtes appalachiennes successives dominent la zone des bas plateaux et des plaines littorales. La côte, tantôt basse et tantôt rocheuse, est en voie de régularisation. <br><br>

              <b>LE CLIMAT</b> <br>
              Le Congo reçoit plus de 1 200 mm de pluie par an, sauf dans la vallée du Niari ; mais les précipitations dépassent rarement 1 700 mm. Leur rythme est marqué par l'existence d'une saison sèche, longue de quatre mois dans le Sud (juin-septembre), qui dure encore un mois sous l'équateur et pendant laquelle la température s'abaisse nettement. La « petite saison sèche » de décembre-janvier n'est qu'un ralentissement des pluies. La chaleur moyenne est comprise entre 24 et 26 °C, et les maximums ne sont jamais très élevés. Le fleuve Congo et l'Oubangui servent de frontière avec la République démocratique du Congo ; le Niari Kouilou a un bassin purement congolais. Les régimes hydrologiques sont très variables. Celui du Kouilou (appelé Niari, en amont de Makabana), dont le module est de 960 m3/s, reflète les variations pluviométriques en présentant un étiage marqué et deux périodes de hautes eaux. Celui de l'Oubangui est nettement tropical : une seule crue, très accentuée. <br>Le régime du Congo est rendu très complexe par l'étendue de son bassin : ses variations à Brazzaville sont souvent le reflet de phénomènes qui se sont produits très loin de là. <br><br>

              <b>LA VÉGÉTATION</b> <br>
              La forêt, humide, dense, à la végétation étagée, couvre 19 000 000 d'ha répartis entre trois grands blocs : la chaîne du Mayombe, le massif du Chaillu et le nord du pays. Mais, dans la cuvette congolaise, le sol marécageux la rend difficilement exploitable. Les savanes occupent le reste du territoire, déroulant leur tapis de hautes herbes piqueté de millions d'arbres, d'arbustes ou de buissons ; elles sont parcourues chaque année par de vastes incendies. Zones inondées et lacs sont encombrés par des prairies flottantes ou des papyraies. La mangrove est installée dans quelques estuaires. <br> <br>

              <b>LA POPULATION ET L'ÉCONOMIE</b> <br>
              Le Congo est un pays peu peuplé, avec une densité moyenne à peine supérieure à 10 habitants par km2. Longtemps, la fécondité y fut inférieure à la moyenne de l'Afrique subsaharienne, ce qui tenait à une stérilité féminine assez importante. Cette spécificité a disparu et le taux de croissance démographique a rejoint la norme africaine, avec, corrélativement, un rajeunissement de la population : avec un âge médian de 16 ans, la population du Congo est aujourd'hui une des plus jeunes du monde, derrière celles du Niger et de l'Ouganda. <br> <br>

              La population est très inégalement répartie. Le Sud, où se situent les deux principales villes du pays, regroupe près des trois quarts des habitants. Le taux d'urbanisation est l'un des plus élevés d'Afrique. <br> <br>

              Le Congo a hérité de la période coloniale une économie déséquilibrée, la prépondérance des activités de services datant de l'époque où Brazzaville était la capitale de l'A.-É.F. (Afrique-Équatoriale française). La mise en valeur de gisements pétroliers, permettant une extraction annuelle de l'ordre de 12 à 14 millions de tonnes (soit, par habitant, quatre à cinq fois la production du Nigeria), n'a fait qu'accentuer cette tendance, et le Congo est, dès lors, entré dans un système d'économie de rente : le pétrole représente les deux tiers du produit intérieur brut et 80 % du total des exportations. L'agriculture, qui n'a jamais été très dynamique, ne parvient toujours pas à satisfaire les besoins alimentaires de base des villes et le pays doit recourir massivement aux importations. L'exploitation du bois est en régression, la production de potasse interrompue et celle de sucre en constante diminution. Seule la fabrication de boissons et de cigarettes parvient à maintenir une activité relativement soutenue. Quant aux infrastructures, elles n'ont été ni développées, ni même entretenues : l'exploitation du chemin de fer est devenue si incertaine que le Gabon a décidé, en 1991, d'utiliser son propre réseau pour acheminer son minerai de manganèse. La route qui relie Brazzaville à Pointe-Noire, la capitale économique située au bord de l'Atlantique, est toujours en construction. <br> <br>

              Bien que quatrième pays producteur d'hydrocarbures en Afrique subsaharienne, le Congo ne bénéficie guère de ses retombées, du fait du montant très élevé de sa dette extérieure (environ 150 % du produit intérieur brut en 2005), qui absorbe à elle seule 60 % des recettes budgétaires. Les importations restent supérieures aux exportations. La part des produits manufacturés dans les exportations est passée de 15 % dans les années 1990 à seulement 2 % aujourd'hui. Aussi le pays recherche-t-il l'aide du Fonds monétaire international dans le cadre de l'initiative de réduction de la dette en faveur des pays pauvres très endettés, tout en essayant de diversifier son économie (exploitation du fer et du magnésium).
            </p>
