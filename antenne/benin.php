        <h3>PAYS D’IMPLANTATION</h3>
            <p style="text-align: justify;">
              Le Bénin est un État d'Afrique de l'Ouest, qui couvre une superficie de 114 763 km2 et s'étend sur 700 km4, du fleuve Niger au nord à la côte atlantique au sud. Le Bénin comptait <b>10 741 458</b> habitants en 2016. Le pays fait partie de la CEDEAO et a comme voisins le Togo à l'ouest, le Nigeria à l'est, le Niger au nord-est et le Burkina Faso au nord-ouest. <br><br><br>

              <b>Relief</b><br>
              De forme étirée entre le fleuve Niger au nord et la plaine côtière dans le sud, le relief de l'ensemble du pays est peu accidenté. <br><br>

              Le nord du pays est principalement constitué de savanes et de montagnes semi-arides, telles que la chaîne de l'Atacora, qui se prolonge au Togo et au Ghana d'une part et au Niger d'autre part. Le point culminant du pays est le mont Sokbaro (ou Sagbarao) (800 mètres). <br><br>

              Le sud du pays est constitué d'une plaine côtière basse parsemée de marécages, lacs et lagunes, notamment la basse vallée de l'Ouémé, la lagune de Porto-Novo et le lac Nokoué, une vaste zone humide de 91 600 ha reconnue d'importance internationale par la convention de Ramsar. <br><br>

              La majeure partie de la population vit dans les plaines côtières méridionales, dont l'altitude ne dépasse nulle part 10 m. C'est là que les plus grandes villes du Bénin sont concentrées, notamment Porto-Novo, la capitale officielle, et Cotonou, la capitale économique et politique. <br><br>


              <b>Climat</b><br>
                Le climat du pays, situé dans une zone intertropicale, est globalement chaud et humide, avec des nuances saisonnières et géographiques liées à la latitude, au relief et à l'alternance des saisons. Il oppose deux zones séparées par le 10e parallèle : au sud, un régime subéquatorial tempéré ; au nord, climat plus chaud et sec.<br><br>

                Il tombe entre 900 et 1 300 millimètres d'eau par an, les régions les plus arrosées sont situées au sud-est, de Cotonou à Porto-Novo, l'Atacora entre Natitingou et Djougou, les régions de Dassa-Zoumè et de N'Dali au nord de Parakou. Les maximums des précipitations sont au sud (climat équatorial), de la mi-mars à la mi-juillet, et plus faiblement en novembre et décembre.<br><br>

                La mousson, océanique et chargée d'humidité, souffle d'avril à novembre, du sud-ouest. L'harmattan continental et sec, souffle dans le sens inverse de la mousson (il vient du nord, du Sahel), de novembre à mai, apportant une poussière ocre orange.<br><br>

                Le taux d'humidité, toujours important, se situe entre 65 et 95 %. La moyenne des températures est comprise entre 22° et 34 °C, avril et mai étant les mois les plus chauds, juste après que l'harmattan a soufflé durant six mois, avant que la mousson n'apporte ses pluies. <br><br><br>


                <b>Économie</b> <br>

                Le Bénin est membre de l'Union économique et monétaire ouest-africaine <b>(UEMOA)</b>. Sa monnaie est le <b>franc CFA.</b> <br><br>

                Il appartient au groupe des pays les moins avancés. En termes d’indice de développement humain (IDH), il se classe au 163e rang sur 189 du rapport PNUD 201740, reculant de deux places par rapport à 201640. Pour 2020, il se situe au 149e rang sur 190 du rapport Doing business, qui calcule l'indice de la facilité de faire des affaires, contre 153e l'année précédente.<br><br>

                Pourtant le taux de croissance, tombé à 2.5 % en 2010 (le plus bas de l’Afrique de l’Ouest), est depuis 2011 légèrement supérieur à 5 %. Mais la croissance, la baisse de l'inflation et le développement des infrastructures ne suffisent pas pour réduire la pauvreté globale, du fait de l'absence de redistribution, de la pression démographique et de la présence d’un secteur informel très important, en forte progression depuis les années 1980. <br><br>

                Le port autonome de Cotonou (PAC) constitue l'un des pivots de l'économie béninoise46. Cependant, 80 % des marchandises importées sont réexportées vers le Nigeria, ce qui rend le pays très dépendant de son puissant voisin.<br><br>

                L'autre source de richesses est le coton, culture qui a réussi d'excellentes récoltes, même si sur le marché mondial, le cours de la livre de la fibre était en 2015 autour de 0,70 dollar, relativement bas comparé au pic des 2 dollars la livre qu’il avait atteint en 2011. Le Bénin était à la quatrième place du palmarès des sept premiers producteurs africains de coton au milieu des années 2010.<br><br>

                Les principales cultures sont surtout vivrières : igname, manioc, maïs, sorgho, riz paddy, fruits et légumes.<br><br>

                L'élevage concerne principalement le cheptel bovin qui comptait 2 111 000 têtes des ovins (842 000), des caprins (1 674 000), des porcins (398 000) et des volailles (17 683 000) selon les données de la Direction de l’Elevage (2012).<br><br>

                Le cheptel national des porcs est constamment sous la menace de la peste porcine africaine (PPA) qui sévit sous une forme en zootique.<br><br>


                Évolution historique du produit intérieur brut par habitant du Bénin. <br>
                L’élevage non conventionnel prend de l’ampleur avec un nombre de plus en plus important d’éleveurs de poules pondeuses, de lapins, d’escargots, de poulets chair et d’abeilles. L’agribusiness se développe avec un engouement des jeunes agripreneurs qui développent plusieurs initiatives en se basant sur les technologies de l’information et de la communication. Des sites internet (le blog de Louis Agbokou par exemple), des plateformes de ventes en ligne de produits agricoles (la plateforme BenAgri par exemple), des magazines spécialisés (La Voix Rurale par exemple), des groupes WhatsApp et Facebook sont créés entre acteurs du secteur agricole. Ces types d’acteurs principalement les jeunes discutent entre eux des préoccupations afférentes à leurs activités et contribuent au développement du secteur agricole.<br><br>

                La pêche reste souvent artisanale, elle est concurrencée par les bateaux étrangers10. On note également le développement de la pisciculture avec pour espèces élevées le Clarias et le Tilapia.<br><br>

                Le tourisme représente 2.5 % du PIB du pays, qui occupe la cinquième place des destinations en Afrique de l'Ouest. En 2013 il a accueilli 230 946 visiteurs étrangers contre 219 949 en 2012. Ses principaux atouts sont les plages et les cités lacustres du sud (Ganvié), les parcs animaliers au nord (Pendjari et W), Abomey et ses palais royaux, Ouidah, lieu de mémoire de l'esclavage et berceau du culte vaudou. <br><br><br>


                <b>Religions</b><br>

                La Constitution de 1990 proclame la laïcité de l'État et la liberté de pensée, d'expression et de pratiques religieuses. Le Bénin est un pays membre de l'Organisation de la coopération islamique. <br><br>

                D'après le recensement de 2013 (RGPH4), 27,7 % des habitants sont musulmans, 25,5 % catholiques, 11,6 % sont praticiens du vaudou, 6,7 % appartiennent à l’Église du christianisme céleste. Il existe d'autres communautés rassemblant moins de 5 % de la population, telles que les méthodistes, les adeptes de l'Église de Jésus-Christ des saints des derniers jours (mormons), les Témoins de Jéhovah, les bahaïs, les baptistes, les pentecôtistes, les membres de l’Église de l'Unification (Moon) et les eckankars. 5,8 % de la population se déclarent sans affiliation religieuse.<br><br>

                Les statistiques ne reflètent pas la place réelle du vaudou au Bénin, son berceau historique au xviie siècle. En effet de nombreux Béninois associent sa pratique à celles d'autres religions. D'abord diabolisé par les voyageurs, combattu par les missionnaires, interdit dans les années 1970 sous le régime marxiste de Kérékou, le culte du vaudou est l’objet, depuis 1993, d’une fête nationale, célébrée le 10 janvier et de plus en plus populaire.

            </p>


