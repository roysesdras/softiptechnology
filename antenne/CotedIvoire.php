        <h3 style="color: #305196;">PAYS D’IMPLANTATION</h3>
            <p style="text-align:justify;">
              La Côte d’Ivoire est un pays situé en Afrique de l’ouest, dans l’hémisphère nord, entre le tropique du Cancer et l’Equateur.
                      Elle est délimitée au nord par le Burkina-Faso et le Mali. A l’ouest, par le Libéria, au Nord- ouest par la Guinée, à l’est par le Ghana et est bordée au sud par l’océan Atlantique.
                      Sa superficie est de 322.462 km2, pour une population d’environ 29 389 150 habitants.
                      Le pays a pour capitale politique, la ville de Yamoussoukro et pour capitale économique celle d’Abidjan. Et est divisée en régions. Il en existe 31, plus  deux Districts autonomes : Abidjan et Yamoussoukro.
                      La monnaie utilisée est le franc CFA, et la langue officielle parlée, le français. Il existe une soixantaine d’ethnies locales. <br><br>

                      <b style="color: #d4a90a;">LE RELIEF</b> <br>
                      La Côte d’Ivoire présente dans son ensemble un relief peu accidenté, avec des plaines et des plateaux ; excepté l’ouest qui est plus montagneux et plus élevée, avec des plateaux qui culminent à plus de 1000 m. Le Mont Nimba (1752m) située dans la région de Man, relie les frontières ivoiriennes à celles de la Guinée et du Libéria.
                      Le sud du pays, le long du Golf de Guinée offre des falaises dans sa partie ouest et une bande de sable et de lagune dans sa partie est.
                      Au nord de cette bande littoral, se trouve une vaste plaine puis une région de bas plateaux pouvant atteindre des altitudes inférieures à 350 mètres. <br><br>

                      <b style="color: #d4a90a;">LE CLIMAT</b> <br>
                      La Côte d’Ivoire est la zone de transition entre le climat équatorial humide et le climat tropical sec, ce qui permet au pays d’être divisé en deux zones principales : le sud et le nord. <br>
                      La différence entre ces deux parties se situe au niveau du taux d’hygrométrie qui peut parfois avoisiner 100% dans le sud et descendre à 20% dans le nord par temps d’harmattan (vent frais et sec en provenance du Sahara).
                      Dans le sud, en dessous de Yamoussoukro, le climat est équatorial donc très humide. Avec des températures allant de 29° à 32° Celsius.
                      Aussi distingue-t-on quatre (saisons) <br>
                      La grande saison des pluies : d’Avril à la mi-juillet, avec de fréquentes précipitations et de nombreux orages, <br>
                      La petite saison sèche : de la mi-juillet à Septembre, ou le ciel reste couvert, <br>
                      La petite saison des pluies : qui part de Septembre à Novembre, avec quelques petites précipitations et, <br>
                      La grande saison sèche : de Décembre à mars, marquée par les alizés du nord (harmattan). <br>
                      Le nord, lui présente un climat tropical sec, avec des températures qui varient entre 28° et 37°Celsius. Ici l’on ne distingue que deux saisons principales : <br>
                      La grande saison des pluies allant de  Juin à Septembre, due aux alizés humides et
                      la grande saison sèche, qui part d’Octobre à Mai. <br><br>

                      <b style="color: #d4a90a;">LA VEGETATION</b> <br>
                      En raison des deux zones climatiques nord et sud, la Côte d'Ivoire est séparée en deux zones de végétation : la forêt au sud, la savane au nord. <br>
                      La limite est constituée par une ligne qui part de Man à l’ouest, qui redescend dans la région de Yamoussoukro et remonte aux environs de Bondoukou à l'est, d’où son nom le V baoulé. <br>
                      La partie nord est couverte de savanes, caractérisées par de grands espaces d'herbage et d'arbres clairsemés, surtout à l'approche du Sahel au nord. Seules les zones proches des cours d'eau présentent des forêts denses et une végétation riche. Les principales plantations sont les champs de mil, de millet, de sorgho, de riz, d’anacarde, de mangue, d’arachide et de coton, et vers le sud des maraîchers de tomates et de légumes.
                      Dans le centre, de vastes plantations de café et de cacao ont largement pris la place de la forêt. <br>
                      La forêt s'étend sur toute la partie sud du pays. Sa surface a fortement diminué dans les dernières décennies, en partie à cause d'une exploitation excessive. La forêt primaire a pratiquement disparu, en dehors de certaines zones protégées, comme les parcs nationaux de Taï au sud-ouest et du Banco dans les environs d'Abidjan. On trouve dans le sud des cultures d'ananas, de bananes, d'hévéas, de cacao et de café, ainsi que des cocotiers. <br> <br>

                      <b style="color: #d4a90a;">LES FLEUVES</b> <br>
                      La Côte d'Ivoire est traversée par quatre fleuves principaux, avec de nombreux affluents. Ces fleuves coulent dans le sens nord-sud, leur cours se terminant dans le golfe de Guinée : <br>
                      Le Cavally (600 km), à l'extrême ouest, prend sa source en Guinée et forme la frontière naturelle avec le Libéria,<br>
                      Le Sassandra (650 km), à l'ouest, prend également sa source en Guinée, <br>
                      Le Bandama (950 km), au centre, est le seul fleuve ayant entièrement son bassin en Côte d'Ivoire, <br>
                      La Comoé (900 km), à l'est, prend sa source au Burkina Faso. 
                      Ces fleuves ne sont pratiquement pas navigables, notamment à cause des rapides et chutes d'eau, ou à cause du manque de débit en saison sèche. <br><br>

                      <b style="color: #d4a90a;">LA TERRE DU CACAO EN QUELQUES MOTS</b> <br>
                      La Côte d’Ivoire c’est environ 550km de côte donnant sur la mer ; des plans d’eau lagunaires à vous couper le souffle pour tous types de sports nautiques, des criques, du sable fin. C’est également de nombreuses espèces animales et végétales, des réceptifs hôteliers de hauts standings avec toutes les commodités, des représentations de grands groupes hôteliers, et bancaires internationaux, un florilège de cultures aussi variées que captivantes, l’originalités des mets culinaires, des sites et curiosités à découvrir qui vous marqueront.
                      La Côte d’Ivoire, terre du cacao c’est avant tout le pays du sourire et de l’hospitalité.

            </p>
