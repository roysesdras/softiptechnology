        <h3>PAYS D’IMPLANTATION</h3>
            <p style="text-align:justify;">
              État d'Afrique de l'Ouest, le Togo est bordé au nord par le Burkina, à l'est par le Bénin, au sud par l'océan Atlantique (le golfe de Guinée) et à l'ouest par le Ghana. <br><br>

              <b>Superficie :</b> 56 600 km2 <br>
