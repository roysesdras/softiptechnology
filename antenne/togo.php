              <b>GÉOGRAPHIE</b><br>
              Pays au climat tropical, de moins en moins humide du sud (forêts) au nord (savanes), le Togo demeure essentiellement rural. Les exportations de produits agricoles (palmistes, café, cacao, coton) viennent cependant loin derrière celles des phosphates du lac Togo, seule ressource notable du sous-sol. Le manioc et le maïs constituent les bases de l'alimentation. <br><br>

              <b>LE MILIEU NATUREL</b> <br>
              Étiré sur 600 km du nord au sud, depuis le Burkina (11 ° de latitude nord) jusqu'au golfe de Guinée (5 ° de latitude nord), le Togo forme un étroit couloir, large seulement d'une centaine de kilomètres, entre le Ghana et le Bénin. Des plateaux et de larges plaines alluviales, de part et d'autre d'une chaîne montagneuse prenant le pays en écharpe, constituent les traits dominants du relief. La chaîne de l'Atakora correspond à des alignements quartzitiques de direction S.-O./N.-E., d'altitude moyenne (800 m). La plaine orientale coïncide avec les bassins du fleuve Mono et des deux petites rivières Haho et Sio qui se jettent dans le lac Togo, communiquant avec la lagune Anécho. Cette plaine est séparée de la zone lagunaire et littorale par le plateau de « terre de barre », sablo-argileux, entaillé par la dépression de la Lama. La plaine d'inondation de l'Oti, fermée au nord par le plateau de Dapango, appartient au bassin hydrographique de la Volta. <br><br>

              Le climat résulte de la confrontation entre l'alizé boréal (harmattan), flux d'air tropical continental sec et chaud, de secteur nord-est, envahissant le Togo en hiver, et l'alizé austral, humide, la mousson, de direction S.-O./N.-E., pénétrant plus ou moins profondément à l'intérieur du pays en été. On distingue deux régimes climatiques : au sud, un climat subéquatorial à quatre saisons alternées, dont une grande saison des pluies (hivernage, d'avril à juillet), et une grande saison sèche (de décembre à mars) ; au nord, un climat tropical à deux saisons très contrastées. On notera toutefois deux anomalies : la dorsale pluviométrique de l'Atakora (1 500 mm) et l'anomalie négative littorale expliquant la présence de baobabs à Lomé (800 mm). <br><br>

              La végétation est le reflet de la pluviométrie ; les formations forestières de type ombrophile occupent le massif de l'Atakora. Le bassin du Mono est le domaine de la savane guinéenne, alors que la plaine de l'Oti est couverte de savanes soudaniennes. <br><br>

              <b>LA POPULATION ET L'ÉCONOMIE</b> <br>
              La population du Togo se caractérise par son dynamisme démographique, avec un taux d'accroissement naturel de 2,7 % par an, sa grande jeunesse (43 % des Togolais ont moins de 15 ans) et son inégale distribution spatiale. La densité de peuplement, moyenne à l'échelle du pays (84 habitants par km2), est particulièrement forte dans le Sud où elle dépasse 200 habitants par km2. Le Togo demeure un pays essentiellement rural. La population urbaine, qui a été multipliée par six depuis l'indépendance et concerne désormais plus d'un habitants sur trois, est surtout concentrée dans le Sud où se situe la capitale, Lomé (800 000 habitants), à la frontière avec le Ghana et dont le port autonome est assez actif et accueille une zone franche industrielle. Nord et Sud s'opposent également par l'origine des peuples : groupe Kwa, dans lequel prédominent les Éwés, dans le Sud, plus christianisé ; groupe Gour, dominé par les Kabyès, dans le Centre et le Nord.
              <br><br>
              L'extension en latitude du pays autorise une assez grande variété de productions rurales, l'agriculture employant plus des deux tiers des actifs. Les cultures vivrières (manioc, maïs, mil, arachide, fruits) et l'élevage ovin, essentiellement tournés vers la satisfaction des besoins intérieurs, alimentent un commerce actif. Si l'industrie est quasi inexistante, l'artisanat – le secteur informel – est très développé, notamment dans les agglomérations. Les principaux produits d'exportation sont les phosphates du lac Togo, seule ressource notable du sous-sol, et les cultures commerciales (café, coton, cacao, palmistes). L'élevage bovin et la pêche demeurent modestes. La balance commerciale est structurellement déficitaire et ne tient pas compte des intenses échanges transfrontaliers facilités par l'existence d'un réseau de transport (routes et voies ferrées) relativement dense. <br><br>

              L'économie du Togo a été mise à mal par la grave crise politique des années 1990 à 1993. Le pays, très endetté, privé de l'aide internationale depuis ces troubles, connaît une situation très précaire. La production de phosphates s'est effondrée de moitié, les échanges ont fortement ralenti, des habitants du Sud ont fui la répression, abandonnant leurs activités, et les finances publiques se sont totalement dégradées. Le maintien d'un appareil d'État plus qu'autoritaire n'est pas favorable à un véritable développement économique.
            </p>
