        <h3>PAYS D’IMPLANTATION</h3>
            <p style="text-align:justify;">

                Burkina ou Burkina Faso <br>
                anciennement Haute-Volta <br><br>

                Nom officiel : République du Burkina<br><br>

                État d'Afrique de l'Ouest, le Burkina est limité au Sud par le Bénin, le Togo, le Ghana et la Côte d'Ivoire, à l'Est par le Niger, au Nord et à l'Ouest par le Mali.
                <br><br>
                <b>Superficie :</b> 275 000 km2<br>
                <b>Nombre d'habitants :</b> 20 321 378 (estimation pour 2019)<br>
                <b>Nom des habitants :</b> Burkinabés<br>
                <b>Capitale :</b> Ouagadougou<br>
                <b>Langue :</b> français<br>
                <b>Monnaie :</b> franc C.F.A.<br>
                <b>Chef de l'État :</b> Paul-Henri Sandaogo Damiba<br>
                <b>Chef du gouvernement :</b> Albert Ouédraogo<br>
                <b>Nature de l'État :</b> république à régime semi-présidentiel<br>
                <b>Constitution :</b><br>
                <b>Adoption :</b> 2 juin 1991<br>
                <b>Entrée en vigueur :</b> 11 juin 1991<br>
                <b>Révision :</b> janvier 1997, avril 2000, janvier 2002<br><br>

                <b>GÉOGRAPHIE</b> <br>
                Savane <br>
                Enclavé au cœur du Sahel, c'est un pays pauvre, souvent aride, domaine d'une médiocre agriculture vivrière (sorgho, mil) avec quelques plantations commerciales (coton, arachide). L'élevage (ovin et bovin) souffre des fréquentes sécheresses. Quelques ressources minières commencent à être exploitées (or, zinc...). Les Mossi forment l'ethnie principale d'un pays aujourd'hui largement islamisé.<br><br>

                <b>LE MILIEU NATUREL</b> <br>
                Le Burkina comprend une zone plane, vers 300 m d'altitude, correspondant à un affleurement du socle, flanquée à l'ouest et au sud-est de deux plateaux sédimentaires (grès pour l'essentiel). Ces derniers dressent, au-dessus de la zone centrale, des rebords de 100 à 300 m de dénivellation ou « falaises ». Dans le Nord, l'ensemble est masqué par des cordons dunaires, d'origine quaternaire, étirés d'ouest en est et en partie fixés. <br><br>

                Le climat est soudano-sahélien, avec l'alternance d'une longue saison sèche (6 à 8 mois, de novembre à mai-juin), d'abord fraîche, puis très chaude, et d'une saison humide. Le total des précipitations diminue du sud vers le nord (1 200 mm à Banfora, 750 à Ouagadougou, 590 à Dori), avec des variations d'une année sur l'autre. <br><br>

                Le réseau hydrographique regroupe pour l'essentiel les hauts cours du Mouhoun (Volta Noire), du Nakambe (Volta Blanche) et du Nazinon (Volta Rouge), qui prennent leur source, la première sur les plateaux occidentaux et les deux autres dans la plaine. Le régime reflète assez fidèlement le rythme des précipitations, avec des débits faibles à nuls en saison sèche, et de hautes eaux en hivernage. Celles-ci sont fréquemment retenues à l'amont de petits barrages en terre. <br><br>

                Les sols, acides et souvent cuirassés, sont de qualité médiocre. Ceux des vallées, les plus riches, ont été délaissés par les populations à cause de l'onchocercose.  <br><br>

                La formation végétale la plus fréquente est la savane, avec un tapis de graminées sous un étage arboré. Mais, si dans le Sud elle est à la limite de la forêt claire, dans le Nord elle fait place à une steppe, essentiellement formée de graminées auxquelles s'ajoutent quelques buissons et quelques épineux. <br><br>

                <b>LA POPULATION</b> <br>
                Les Mossis, majoritaires, représentent près de 50 % de la population. Le reste se partage entre de nombreuses ethnies (Peuls au nord, Bobos à l'ouest, Bissas et Gourmantchés à l'est…). Le Burkina connaît, depuis quelques décennies, une véritable explosion démographique. De 4,5 millions d'habitants en 1960, la population est passée à 12,3 millions en 2001 : 47,3 % d'entre eux sont âgés de moins de 15 ans. Ces chiffres donnent la mesure du problème démographique burkinabé – 6,8 enfants par femme –, qui doit être rapproché du faible potentiel d'un pays sahélien peu favorisé en ressources naturelles et, de plus, sans débouché sur la mer. Le principal mode de régulation a toujours été l'émigration, non pas tant vers le monde développé que vers les pays côtiers voisins, notamment le Ghana, puis la Côte d'Ivoire, au gré des opportunités du marché du travail. On estimait que les Burkinabé représentaient plus de 10 % de la population ivoirienne vers 1990. <br><br>

                Les habitants sont inégalement répartis sur le territoire : la densité moyenne est de 45 habitants par km2, mais elle ne dépasse guère 10 à 15 habitants par km2 au nord et à l'est, alors qu'elle peut atteindre jusqu'à 100 habitants par km2 (voire davantage sur le plateau mossi) dans le centre-sud du pays, où se trouve la capitale, Ouagadougou. Cette ville, tout comme Bobo-Dioulasso, dans le sud-ouest, connaît une forte croissance. Malgré cela, le taux de population urbaine (17 % de citadins, contre 5 % vers 1960) demeure moins élevé que dans beaucoup de pays africains, ce qui explique, pour une part, la faiblesse de la scolarisation – en particulier des filles – et la lenteur de l'acceptation de l'idée de contrôle des naissances. Le Burkina pourrait ainsi avoir 15 millions d'habitants aux alentours de 2010. <br><br>

                Les religions traditionnelles sont toujours présentes, mais elles reculent devant la forte poussée de l'islam, que pratique au moins la moitié de la population. Le christianisme, lui aussi en progression, reste minoritaire (moins de 15 %).        <br><br>

                <b>L'ÉCONOMIE</b> <br>
                Le Burkina est généralement considéré comme un pays rural, et à cette image s'associe facilement celle d'archaïsme, justifiant les interventions d'une multitude d'ONG (organisations non gouvernementales) étrangères, soucieuses de promouvoir le développement. La réalité est moins simple. D'ores et déjà, l'agriculture et l'élevage représentent moins de la moitié de la richesse nationale produite chaque année (PIB). Si les industries extractives (or, zinc, manganèse, bauxite) demeurent peu importantes, les activités de transformation progressent. Certes, le nombre des usines reste faible et se limite à quelques unités de production de biens de consommation (boissons, minoteries, textile, montage de cycles, chaussures). Par contre, ce que l'on a coutume d'appeler le « secteur informel » (qui échappe largement au contrôle de la puissance publique) est très répandu, surtout en ville, où il offre la majorité des emplois disponibles et permet de vendre des biens et des services indispensables à des prix abordables pour une population à faible revenu : construction, matériaux divers, mobilier, tissus et vêtements, coiffure, restauration rapide, réparations en tout genre… Commerces et transports « informels » sont ainsi omniprésents.      <br><br>

                Le secteur rural a vécu de profonds changements, tout en subissant le choc de la grande sécheresse des années 1970 dans la zone sahélienne. Du côté des productions vivrières, le maïs, qui a connu un rapide développement, reste encore loin des céréales traditionnelles (mil et sorgho). Autour des villes, voire à l'intérieur de celles-ci, les cultures maraîchères se sont répandues et ont amené une sensible transformation des habitudes alimentaires des citadins. Corrélativement, les cultures vivrières, qui étaient essentiellement auto consommées il y a une trentaine d'années, font maintenant l'objet d'un commerce actif. Quant aux cultures industrielles et/ou d'exportation (arachide, canne à sucre, légumes…), elles auraient tendance à se diversifier et sont surtout marquées par la croissance du coton, qui a suscité un réel engouement chez les paysans en raison des revenus monétaires relativement élevés qu'il procure. D'une manière générale, les techniques évoluent, avec, en particulier, le développement de la culture attelée. <br><br>

                L'élevage se remet peu à peu des conséquences catastrophiques de la sécheresse. Une de ses principales caractéristiques est l'importance des animaux de ferme (chèvres, moutons, volailles), alors que les troupeaux de bovins du nord du pays, élevés par les Peuls, connaissent une certaine stagnation. Grâce aux accords passés avec le F.M.I. et la Banque mondiale, des programmes de développement rural et hydraulique ont été engagés. Les barrages de Kompienga et de Bagré devraient ainsi permettre, à terme, l'autosuffisance alimentaire et énergétique. <br><br>

                Pour un pays enclavé comme le Burkina, les liaisons avec l'extérieur sont essentielles. Une voie ferrée relie Ouagadougou à Abidjan (en cours de prolongement jusqu'à Tambao, à l'extrême nord, où est exploité un gisement de manganèse), et des routes bitumées, construites grâce aux aides extérieures, rejoignent la plupart des pays voisins. Les échanges avec le Sud sont particulièrement intensifs. La balance commerciale (marchandises) est très déséquilibrée, les exportations couvrant environ les deux tiers des importations depuis la dévaluation du franc CFA, en janvier 1994. Ce déficit est en grande partie comblé par des transferts monétaires : aides publiques extérieures et rapatriement de revenus des émigrés. La France demeure le principal partenaire commercial et l'un des principaux bailleurs de fonds occidentaux. La pauvreté du pays n'autorise pas des finances publiques florissantes. Néanmoins, elles ont généralement fait l'objet d'une gestion prudente de la part des gouvernements, ce qui a permis de limiter l'endettement extérieur.
            </p>
