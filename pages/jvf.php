              Chaque année au mois d’octobre la Journée du Volontariat Français met en lumière l’engagement des volontaires mobilisés sur le terrain.  Cet événement est porté par France volontaire, la plateforme française du volontariat international d’échange et de solidarité. <br>
              Dans 22 des Espaces Volontariats que compte France Volontaires en Afrique, en Amérique latine et Caraïbes et en Asie, ce grand rendez-vous annuel réunit les acteurs du volontariat, institutionnels et associatifs, structures d’accueil et d’envoi, partenaires et volontaires locaux et internationaux. Signé à ce jour par plus de 200 organisations dans le monde, l’Appel pour le volontariat de demain vient en réponse à des défis partagés (préservation de l’environnement, mobilités, inclusion sociale et professionnelle) qui appellent des réponses communes. À travers cinq recommandations, les signataires de l’Appel aspirent à insuffler un élan collectif pour que le volontariat de demain contribue activement à l’émergence d’une société de l’engagement solidaire et ouverte sur le monde. <br>
