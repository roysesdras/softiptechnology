            <p style="text-align:justify">
              Le 20 novembre 1989, l’ONU adoptait à l’unanimité la Convention relative aux droits de l’Enfant: les droits de chaque enfant du monde étaient désormais reconnus par un traité international, ratifié par 195 Etats ! Depuis, le 20 novembre a été déclaré Journée internationale des droits de l’enfant – un temps fort que l’UNICEF ne manque pas de marquer chaque année. <br><br>

              En 1954, l’Assemblée générale a recommandé à tous les pays d’instituer une "Journée mondiale de l’enfance", qui serait une journée de fraternité mondiale et de compréhension entre les enfants, et d’activités favorisant le bien-être des enfants du monde entier. Elle a proposé aux gouvernements que cette Journée soit célébrée à la date qui leur semblait la plus appropriée. C'est le 20 novembre qui a été choisi, à la fois jour d'adoption par l’Assemblée de la Déclaration des droits de l’enfant en 1959, et de la Convention relative aux droits de l’enfant, signée en 1989. <br><br>

              Cette Convention, qui est le traité international le plus ratifié en matière de droits de l'homme, définit une liste de droits de l'enfant comprenant le droit à la vie, à la santé, à l'éducation et le droit de jouer, ainsi que le droit à une vie de famille, à être protégé de la violence et de la discrimination, et de faire entendre sa voix. </p>

